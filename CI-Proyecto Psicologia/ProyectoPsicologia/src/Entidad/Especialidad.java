package Entidad;

public class Especialidad {
    int ID_Esecialidad;
    String Especialidad;
    int ID_Servicio;

    public Especialidad() {
    }

    public int getID_Esecialidad() {
        return ID_Esecialidad;
    }

    public void setID_Esecialidad(int ID_Esecialidad) {
        this.ID_Esecialidad = ID_Esecialidad;
    }

    public String getEspecialidad() {
        return Especialidad;
    }

    public void setEspecialidad(String especialidad) {
        Especialidad = especialidad;
    }

    public int getID_Servicio() {
        return ID_Servicio;
    }

    public void setID_Servicio(int ID_Servicio) {
        this.ID_Servicio = ID_Servicio;
    }
}
